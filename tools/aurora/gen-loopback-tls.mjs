#!/usr/bin/env node
/**
 * Generates loopback TLS cert/key for AssetSchemeServer and TlsFingerprint.h (SPKI SHA-256, Base64).
 * Requires openssl on PATH.
 */
import { execSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "../..");
const TLS_DIR = path.join(REPO_ROOT, "runtime", "container", "tls");
const FINGERPRINT_H = path.join(REPO_ROOT, "runtime", "container", "src", "TlsFingerprint.h");

function resolveOpenSsl() {
  const candidates = [
    process.env.OPENSSL_BIN,
    "openssl",
    "C:\\Program Files\\Git\\usr\\bin\\openssl.exe",
  ].filter(Boolean);
  for (const bin of candidates) {
    try {
      execSync(`"${bin}" version`, { stdio: "pipe" });
      return bin;
    } catch {
      /* try next */
    }
  }
  throw new Error("openssl not found; install OpenSSL or Git for Windows");
}

const OPENSSL = resolveOpenSsl();

function run(cmd) {
  return execSync(cmd, { encoding: "utf8", stdio: ["pipe", "pipe", "pipe"] }).trim();
}

function openssl(cmd) {
  return run(`"${OPENSSL}" ${cmd}`);
}

fs.mkdirSync(TLS_DIR, { recursive: true });

const keyPath = path.join(TLS_DIR, "loopback.key");
const crtPath = path.join(TLS_DIR, "loopback.crt");
const cfgPath = path.join(TLS_DIR, "openssl.cnf");

const opensslCnf = `[req]
distinguished_name = req_distinguished_name
x509_extensions = v3_req
prompt = no

[req_distinguished_name]
CN = 127.0.0.1

[v3_req]
subjectAltName = @alt_names

[alt_names]
IP.1 = 127.0.0.1
`;

fs.writeFileSync(cfgPath, opensslCnf);

openssl(
  `req -x509 -newkey rsa:2048 -keyout "${keyPath}" -out "${crtPath}" -days 3650 -nodes -config "${cfgPath}" -extensions v3_req`,
);

const spki = run(
  `"${OPENSSL}" x509 -in "${crtPath}" -pubkey -noout | "${OPENSSL}" pkey -pubin -outform der | "${OPENSSL}" dgst -sha256 -binary | "${OPENSSL}" base64`,
);

const header = `// Auto-generated by tools/aurora/gen-loopback-tls.mjs — do not edit.
#ifndef AUROBORE_TLS_FINGERPRINT_H
#define AUROBORE_TLS_FINGERPRINT_H

namespace Aurobore {
namespace TlsFingerprint {

static const char kSpkiFingerprintBase64[] = "${spki}";

} // namespace TlsFingerprint
} // namespace Aurobore

#endif
`;

fs.writeFileSync(FINGERPRINT_H, header);
console.log(`[gen-loopback-tls] wrote ${crtPath}, ${keyPath}`);
console.log(`[gen-loopback-tls] SPKI fingerprint: ${spki}`);
console.log(`[gen-loopback-tls] wrote ${FINGERPRINT_H}`);
